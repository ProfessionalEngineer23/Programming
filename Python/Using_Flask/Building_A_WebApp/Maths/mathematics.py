
def summation(a, b):
     """
     This function returns the sum of a and b passed in arguments
     """
     result = a + b
     return result 

def subtraction(a, b):
     """
     This function returns the difference of a and b passed in arguments
     """
     result = a - b
     return result

def multiplication(a, b):
     """
     This function returns the multiplication of a and b passed in arguments
     """
     result = a * b 
     return result
